#ifndef _RESULT_H_	// このマクロ定義がされてなかったら
#define _RESULT_H_	// ２重インクルード防止のマクロ定義

//==================================================
// 定義
//==================================================
typedef enum
{
	RESULT_WIN = 0,	// 勝ち
	RESULT_LOSE,	// 負け
	RESULT_DRAW,	// 引き分け
	RESULT_MAX,
	RESULT_NONE
}RESULT;

//==================================================
// プロトタイプ宣言
//==================================================
//--------------------------------------------------
// 初期化
//--------------------------------------------------
void InitResult(void);

//--------------------------------------------------
// 終了
//--------------------------------------------------
void UninitResult(void);

//--------------------------------------------------
// 更新
//--------------------------------------------------
void UpdateResult(void);

//--------------------------------------------------
// 描画
//--------------------------------------------------
void DrawResult(void);

//--------------------------------------------------
// 勝敗の設定
// 引数1  : int nPlayerNo / プレイヤーの番号
// 引数2  : RESULT result / 列挙型 勝敗
//--------------------------------------------------
void SetResult(int nPlayerNo, RESULT result);

//--------------------------------------------------
// 描画するかどうか
// 引数  : bool bDraw / 描画するかどうか
//--------------------------------------------------
void SetDrawResult(bool bDraw);

#endif // !_RESULT_H_
